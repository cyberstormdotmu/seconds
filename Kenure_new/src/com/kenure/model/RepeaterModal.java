package com.kenure.model;

public class RepeaterModal {
	private Integer rowIndex;
	private Integer repeaterLocation;
	private Double latitude;
	private Double longitude;
	private Integer slots;
	private Integer levels;
	
	public Integer getRowIndex() {
		return rowIndex;
	}
	public void setRowIndex(Integer rowIndex) {
		this.rowIndex = rowIndex;
	}
	public Integer getRepeaterLocation() {
		return repeaterLocation;
	}
	public void setRepeaterLocation(Integer repeaterLocation) {
		this.repeaterLocation = repeaterLocation;
	}
	public Double getLatitude() {
		return latitude;
	}
	public void setLatitude(Double latitude) {
		this.latitude = latitude;
	}
	public Double getLongitude() {
		return longitude;
	}
	public void setLongitude(Double longitude) {
		this.longitude = longitude;
	}
	public Integer getSlots() {
		return slots;
	}
	public void setSlots(Integer slots) {
		this.slots = slots;
	}
	public Integer getLevels() {
		return levels;
	}
	public void setLevels(Integer levels) {
		this.levels = levels;
	}
}
