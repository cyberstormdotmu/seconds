package com.kenure.model;

public class TariffTransactionModel {

	private int start; 

	private int end; 

	private Number cost; 

	public int getStart() {
		return start;
	}

	public void setStart(int start) {
		this.start = start;
	}

	public int getEnd() {
		return end;
	}

	public void setEnd(int end) {
		this.end = end;
	}

	public Number getCost() {
		return cost;
	}

	public void setCost(Number cost) {
		this.cost = cost;
	}
	
}
