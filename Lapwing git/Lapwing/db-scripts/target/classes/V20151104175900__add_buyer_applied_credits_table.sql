CREATE TABLE BUYER_APPLIED_CREDITS (
  ID                SERIAL          NOT NULL,
  BUYER_CREDIT_ID   INTEGER 	    NOT NULL,
  STATUS  	        VARCHAR(9)      NOT NULL,
  SPEND_TYPE        VARCHAR(9)      NOT NULL,
  NET_AMOUNT        NUMERIC(9,2)    NOT NULL,
  VAT_AMOUNT        NUMERIC(9,2)    NOT NULL,
  GROSS_AMOUNT      NUMERIC(9,2)    NOT NULL,
  ORDER_ID          INTEGER         NOT NULL,
  CREATED_DATETIME  TIMESTAMP       NOT NULL,

  CONSTRAINT PK_BUYER_APPLIED_CREDITS PRIMARY KEY (ID),
  CONSTRAINT FK_BUYER_APPLIED_CREDITS_ORDER FOREIGN KEY (ORDER_ID) REFERENCES ORDERS(ID),
  CONSTRAINT FK_BUYER_APPLIED_CREDITS_BUYER_CREDITS FOREIGN KEY (BUYER_CREDIT_ID) REFERENCES BUYER_CREDITS(ID)
);