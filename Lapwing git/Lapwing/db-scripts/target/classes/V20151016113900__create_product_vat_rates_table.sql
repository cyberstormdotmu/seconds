CREATE TABLE PRODUCT_VAT_RATES (
  ID SERIAL NOT NULL,
  PRODUCT_ID INTEGER NOT NULL,
  VAT_RATE_ID INTEGER NOT NULL,
  START_DATETIME TIMESTAMP NULL,
  END_DATETIME TIMESTAMP NULL,

  CONSTRAINT PK_PRODUCT_VAT_RATES PRIMARY KEY (ID),
  CONSTRAINT FK_PRODUCT_VAT_RATES_PRODUCTS FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCTS(ID),
  CONSTRAINT FK_PRODUCT_VAT_RATES_VAT_RATES FOREIGN KEY (VAT_RATE_ID) REFERENCES VAT_RATES(ID)
);
