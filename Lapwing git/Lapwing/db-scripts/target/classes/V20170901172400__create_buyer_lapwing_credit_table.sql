CREATE TABLE BUYER_LAPWING_CREDIT
(
  ID 				serial 				NOT NULL,
  BUYER_ID      	INTEGER             NOT NULL DEFAULT 0,
  PENDING_CREDIT 	NUMERIC(9,2),
  AVAILABLE_CREDIT 	NUMERIC(9,2),
  REDEEMABLE_CREDIT NUMERIC(9,2),
  CONSTRAINT PK_BUYER_LAPWING_CREDIT PRIMARY KEY (ID),
  CONSTRAINT FK_BUYER_LAPWING_CREDIT_BUYER FOREIGN KEY (BUYER_ID)
      REFERENCES BUYER_PROFILES (ID) MATCH SIMPLE
      ON UPDATE NO ACTION ON DELETE NO ACTION
)