CREATE TABLE ORDER_LINE_CREDITS (
  ID                SERIAL         NOT NULL,
  ORDER_LINE_ID     INTEGER 	   NOT NULL,
  AMOUNT            NUMERIC(9,2)   NOT NULL,
  CREATED_DATETIME  TIMESTAMP      NOT NULL,

  CONSTRAINT PK_ORDER_LINE_CREDITS PRIMARY KEY (ID),
  CONSTRAINT FK_ORDER_LINE_CREDITS_ORDER_LINES FOREIGN KEY (ORDER_LINE_ID) REFERENCES ORDER_LINE_CREDITS(ID)
);