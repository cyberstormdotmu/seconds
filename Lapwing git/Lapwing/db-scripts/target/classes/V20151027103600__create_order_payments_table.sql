CREATE TABLE ORDER_PAYMENTS (
  ID                SERIAL        NOT NULL,
  ORDER_ID          INTEGER       NOT NULL,
  PAYMENT_REFERENCE VARCHAR(36)   NOT NULL,
  RECEIVED_DATE     TIMESTAMP     NOT NULL,
  PAYMENT_TYPE      VARCHAR(13)   NOT NULL,
  AMOUNT            NUMERIC(9, 2) NOT NULL,
  USER_REFERENCE    VARCHAR(128)  NULL,
  CREATED_DATETIME  TIMESTAMP     NOT NULL,

  CONSTRAINT PK_ORDER_PAYMENTS PRIMARY KEY (ID),
  CONSTRAINT FK_ORDER_PAYMENTS_ORDERS FOREIGN KEY (ORDER_ID) REFERENCES ORDERS (ID)
);
