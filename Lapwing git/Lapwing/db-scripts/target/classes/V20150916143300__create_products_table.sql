CREATE TABLE PRODUCTS (
    ID            SERIAL          NOT NULL,
    NAME          VARCHAR(64)     NOT NULL,
    DESCRIPTION   VARCHAR(256)    NOT NULL,
    VENDOR_ID     INTEGER,
    PRODUCT_CODE  VARCHAR(64),
    SKU           VARCHAR(64),
    VAT_ID        INTEGER,
    PRODUCT_STOCK INTEGER,
    TERMS_AND_CONDITIONS VARCHAR(4096),

  CONSTRAINT PK_PRODUCTS PRIMARY KEY (ID)
);