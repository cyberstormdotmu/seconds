CREATE TABLE BUYER_WITHDRAW_CREDIT
(
  ID 					serial 				NOT NULL,
  BUYER_ID 				INTEGER 			NOT NULL DEFAULT 0,
  REQUESTED_AMOUNT 		NUMERIC(9,2) 		NOT NULL,
  REQUESTED_DATE 		DATE,
  PAYMENT_TYPE 			VARCHAR(256),
  RECEIVED_DATE 		DATE,
  WITHDRAW_REFERENCE 	VARCHAR(256),
  WITHDRAW_STATUS 		BOOLEAN,
  CONFIRM_DATE 			TIMESTAMP,
  CONSTRAINT PK_BUYER_WITHDRAW_CREDIT PRIMARY KEY (ID),
  CONSTRAINT FK_BUYER_LAPWING_CREDIT_BUYER FOREIGN KEY (BUYER_ID)
      REFERENCES BUYER_PROFILES (ID) MATCH SIMPLE
      ON UPDATE NO ACTION ON DELETE NO ACTION
)