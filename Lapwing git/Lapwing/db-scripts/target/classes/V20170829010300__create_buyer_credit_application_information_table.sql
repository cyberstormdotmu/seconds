CREATE TABLE BUYER_CREDIT_APPLICATION_INFORMATION
(
  ID           			serial              NOT NULL,
  BUYER_ID     			INTEGER             NOT NULL DEFAULT 0,
  COMPANY_NAME 			VARCHAR(64)         NOT NULL,
  TRADING_AS 			VARCHAR(64),
  INVOICE_ADDRESS_ID 	INTEGER             NOT NULL DEFAULT 0,
  DELIVERY_ADDRESS_ID   INTEGER 			NOT NULL DEFAULT 0,
  REGISTERED_ADDRESS_ID INTEGER  			NOT NULL DEFAULT 0,
  LANDLINE_NUMBER 		VARCHAR(12) 		NOT NULL,
  WEBSITE 				VARCHAR(256) 		NOT NULL,
  VAT_REGISTRATION		VARCHAR(256) 		NOT NULL,
  PURCHASING_MANAGER 	VARCHAR(256) 		NOT NULL,
  CONTACT_NAME 			VARCHAR(256) 		NOT NULL,
  TELEPHONE_NUMBER 		VARCHAR(12) 		NOT NULL,
  EMAIL_ADDRESS 		VARCHAR(256) 		NOT NULL,
  IS_CASH_ACCOUNT 		BOOLEAN,
  IS_CREDIT_ACCOUNT 	BOOLEAN,
  PRINT_NAME			VARCHAR(256),
  REGISTERED_DATE		DATE,		
  FULL_COMPANY_NAME		VARCHAR(256),
  TRADING_NAME 			VARCHAR(256),
  EXPECTED_CREDIT		NUMERIC(9,2) 		NOT NULL,
  
  CONSTRAINT PK_BUYER_CREDIT_APPLICATION_INFORMATION PRIMARY KEY (ID),
  CONSTRAINT FK_BUYER_CREDIT_APPLICATION_INFORMATION_BUYER FOREIGN KEY (BUYER_ID)
      REFERENCES BUYER_PROFILES (ID) MATCH SIMPLE
      ON UPDATE NO ACTION ON DELETE NO ACTION,
  CONSTRAINT FK_BUYER_CREDIT_APPLICATION_INFORMATION_INVOICE_ADDRESS FOREIGN KEY (INVOICE_ADDRESS_ID)
      REFERENCES ADDRESSES (ID) MATCH SIMPLE
      ON UPDATE NO ACTION ON DELETE NO ACTION,
  CONSTRAINT FK_BUYER_CREDIT_APPLICATION_INFORMATION_DELIVERY_ADDRESS FOREIGN KEY (DELIVERY_ADDRESS_ID)
      REFERENCES ADDRESSES (ID) MATCH SIMPLE
      ON UPDATE NO ACTION ON DELETE NO ACTION,
  CONSTRAINT FK_BUYER_CREDIT_APPLICATION_INFORMATION_REGISTERED_ADDRESS FOREIGN KEY (REGISTERED_ADDRESS_ID)
      REFERENCES ADDRESSES (ID) MATCH SIMPLE
      ON UPDATE NO ACTION ON DELETE NO ACTION
)
