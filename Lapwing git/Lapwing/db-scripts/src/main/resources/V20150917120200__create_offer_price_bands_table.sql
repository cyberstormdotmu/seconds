CREATE TABLE OFFER_PRICE_BANDS (
  ID        SERIAL      NOT NULL,
  OFFER_ID   INTEGER      NOT NULL,
  MIN_VOLUME INTEGER     NOT NULL,
  MAX_VOLUME INTEGER     NULL,
  BUYER_PRICE NUMERIC(9,2) NULL,
  VENDOR_PRICE NUMERIC(9,2) NULL,
  AGENCY_MARGIN NUMERIC(9,2) NULL,
  RESELLER_MARGIN NUMERIC(9,2) NULL,
  DISTRIBUTOR_MARGIN NUMERIC(9,2) NULL,

  CONSTRAINT PK_OFFER_PRICE_BANDS PRIMARY KEY (ID),
  CONSTRAINT FK_OFFER_PRICE_BANDS_OFFERS FOREIGN KEY (OFFER_ID) REFERENCES OFFERS(ID)
);