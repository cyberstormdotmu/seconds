CREATE TABLE ORDER_ADDRESSES (
  ORDER_ID INTEGER NOT NULL,
  IS_INVOICE_ADDRESS BOOLEAN NOT NULL,
  IS_DELIVERY_ADDRESS BOOLEAN NOT NULL,
  ORGANISATION_NAME VARCHAR(256) NOT NULL,
  DEPARTMENT_NAME VARCHAR(64),
  BUILDING_NAME VARCHAR(64),
  STREET_ADDRESS VARCHAR(128),
  LOCALITY VARCHAR(64),
  POST_TOWN VARCHAR(64) NOT NULL,
  POSTCODE VARCHAR(8) NOT NULL,

  CONSTRAINT FK_ORDER_ADDRESSES_ORDER FOREIGN KEY (ORDER_ID) REFERENCES ORDERS (ID)
);